        System.out.println("MIN_VALUE: "+Integer.MIN_VALUE);
        System.out.println("SIZE: "+Integer.SIZE);
        System.out.println("TYPE: "+Integer.TYPE);
        System.out.println("bitCount: "+Integer.bitCount(15));
